class NumArray {
public:
	vector<int> sum;

    NumArray(vector<int> &nums) {
    	int size=nums.size();
    	if (size>0) {
    		sum.push_back(nums[0]);
			for (int i=1; i<size; i++) {
				int num=sum.back();
				sum.push_back(nums[i]+num);
			}
    	}
    }

    int sumRange(int i, int j) {
    	int substract=0;
    	if (i>0) substract=sum[i-1];
    	return sum[j]-substract;
    }
};


