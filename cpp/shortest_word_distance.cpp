  class Solution {
  public:
      int shortestDistance(vector<string>& words, string word1, string word2) {
    	  int shortest=INT_MAX;
    	  int idx1=-1, idx2=-1;
    	  int size=words.size();
    	  for (int i=0; i<size; i++) {
    		  if (words[i]==word1) {
    			  idx1=i;
    			  if (idx2!=-1) {
    				  shortest=min(shortest, idx1-idx2);
    			  }
    		  } else if (words[i]==word2) {
    			  idx2=i;
    			  if (idx1!=-1) {
    				  shortest=min(shortest, idx2-idx1);
    			  }
    		  }
    	  }

    	  return shortest;
      }
  };

